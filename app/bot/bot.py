@dp.message(Command("schedule"))
async def cmd_schedule(message: types.Message):
    """–û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–æ–º–∞–Ω–¥—ã /schedule"""
    try:
        # –ü–æ–ª—É—á–∞–µ–º –≤—Å–µ –≥—Ä—É–ø–ø—ã
        groups = await ScheduleService.get_all_groups()
        
        if not groups:
            await message.answer("üìù –†–∞—Å–ø–∏—Å–∞–Ω–∏–µ –ø–æ–∫–∞ –Ω–µ –¥–æ–±–∞–≤–ª–µ–Ω–æ.")
            return
        
        # –°–æ–∑–¥–∞–µ–º –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É —Å –≥—Ä—É–ø–ø–∞–º–∏
        keyboard = InlineKeyboardMarkup(inline_keyboard=[
            [InlineKeyboardButton(text=group.name, callback_data=f"group_{group.name}")]
            for group in groups
        ])
        
        await message.answer(
            "üìö –í—ã–±–µ—Ä–∏—Ç–µ –≥—Ä—É–ø–ø—É –¥–ª—è –ø—Ä–æ—Å–º–æ—Ç—Ä–∞ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è:",
            reply_markup=keyboard
        )
        
    except Exception as e:
        await message.answer(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è: {str(e)}")

@dp.callback_query(lambda c: c.data.startswith('group_'))
async def process_group_selection(callback_query: types.CallbackQuery):
    """–û–±—Ä–∞–±–æ—Ç—á–∏–∫ –≤—ã–±–æ—Ä–∞ –≥—Ä—É–ø–ø—ã"""
    group_name = callback_query.data.replace('group_', '')
    
    try:
        # –ü–æ–ª—É—á–∞–µ–º —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ –¥–ª—è –≤—ã–±—Ä–∞–Ω–Ω–æ–π –≥—Ä—É–ø–ø—ã
        schedules = await ScheduleService.get_schedule_by_group(group_name)
        
        if not schedules:
            await callback_query.message.answer(f"üìù –†–∞—Å–ø–∏—Å–∞–Ω–∏–µ –¥–ª—è –≥—Ä—É–ø–ø—ã {group_name} –Ω–µ –Ω–∞–π–¥–µ–Ω–æ.")
            return
        
        # –§–æ—Ä–º–∏—Ä—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ —Å —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ–º
        schedule_text = f"üìÖ –†–∞—Å–ø–∏—Å–∞–Ω–∏–µ –≥—Ä—É–ø–ø—ã {group_name}:\n\n"
        
        current_day = ""
        for schedule in schedules:
            if schedule.day_of_week != current_day:
                current_day = schedule.day_of_week
                schedule_text += f"\nüìÜ {current_day}:\n"
            
            time_info = f" ({schedule.time_start}-{schedule.time_end})" if schedule.time_start else ""
            room_info = f" | üè† {schedule.room}" if schedule.room else ""
            teacher_info = f" | üë®‚Äçüè´ {schedule.teacher}" if schedule.teacher else ""
            
            schedule_text += f"{schedule.lesson_number}. {schedule.subject}{time_info}{room_info}{teacher_info}\n"
        
        # –†–∞–∑–±–∏–≤–∞–µ–º –Ω–∞ —á–∞—Å—Ç–∏, –µ—Å–ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ —Å–ª–∏—à–∫–æ–º –¥–ª–∏–Ω–Ω–æ–µ
        if len(schedule_text) > 4096:
            parts = [schedule_text[i:i+4096] for i in range(0, len(schedule_text), 4096)]
            for part in parts:
                await callback_query.message.answer(part)
        else:
            await callback_query.message.answer(schedule_text)
            
    except Exception as e:
        await callback_query.message.answer(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è: {str(e)}")
    
    await callback_query.answer()

